func main() {
	ips := loadIPs()

	var responseMap = make(map[string]*GeoData)
	for _, ip := range ips {
		url := buildUrl(ip)
		resp, err := fetchApi(url)
		if err != nil {
			fmt.Println("Skip IP: ", ip, err)
			continue
		}

		responseMap[ip] = resp
	}

	b, _ := json.Marshal(responseMap)
	fmt.Println(string(b))
}

func buildUrl(ip string) string {
	return fmt.Sprintf("%s/%s", "http://ip-api.com/json", ip)
}

func fetchApi(url string) (*GeoData, error) {
	req, err := http.NewRequest("GET", url, nil)
	if err != nil {
		return nil, err
	}

	client := &http.Client{}
	resp, err := client.Do(req)
	if err != nil {
		return nil, err
	}

	if resp.StatusCode != http.StatusOK {
		return nil, errors.New("failed")
	}

	body, err := io.ReadAll(resp.Body)
	if err != nil {
		return nil, err
	}

	var ipResp GeoData
	err = json.Unmarshal(body, &ipResp)
	if err != nil {
		return nil, err
	}

	return &ipResp, nil
}

type GeoData struct {
	Query       string `json:"query"`
	Status      string `json:"status"`
	Country     string `json:"country"`
	CountryCode string `json:"countryCode"`
	Region      string `json:"region"`
	RegionName  string `json:"regionName"`
	City        string `json:"city"`
	Zip         string `json:"zip"`
	Lat         float64 `json:"lat"`
	Lon         float64 `json:"lon"`
	Timezone    string `json:"timezone"`
	Isp         string `json:"isp"`
	Org         string `json:"org"`
	As          string `json:"as"`
}
